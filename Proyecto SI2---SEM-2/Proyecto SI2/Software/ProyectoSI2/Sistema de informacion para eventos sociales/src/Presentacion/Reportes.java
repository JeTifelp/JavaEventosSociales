    private void DDGENERARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DDGENERARActionPerformed
        LinkedList<String> ser = new LinkedList<>();
        LinkedList<String> col = new LinkedList<>();//nombre de las columnas
        String from = "Paquete,Persona,Cliente,Evento,Contrato";

        if (SID_PAQUETE.isSelected() == true) {
            col.add("Cod_Paquete");
            ser.add("Paquete.Id");
        }
        if (SNOMBRE.isSelected() == true) {
            col.add("Nombre");
            ser.add("Paquete.Nombre");
        }
        if (SPRECIO.isSelected() == true) {
            col.add("Precio");
            ser.add("Paquete.Precio");
        }
        if (CDESCUENTO.isSelected() == true) {
            col.add("Descuento");
            ser.add("Paquete.Descuento");
        }
        if (CPRECIO.isSelected() == true) {
            col.add("Precio_Total");
            ser.add("Paquete.PrecioTotal");
        }
        if (cnoC.isSelected() == true) {
            col.add("Cliente");
            ser.add("Persona.Nombre");
        }
        if (ceve.isSelected() == true) {
            col.add("Evento");
            ser.add("Evento.Descripcion");
        }
        String dwhere="Persona.CI=Cliente.CI AND Contrato.IdPaq=Paquete.Id AND Contrato.IdEven=Evento.Id AND Contrato.CIC=Cliente.CI";
        if (ser.size() > 0) {
            try {
                if (jcServicio.getSelectedItem()=="NINGUNO") {
                    DinamcReport.mostrarRep(NOM_SER.getText(), col, ser, from, dwhere);
                }
                if (jcServicio.getSelectedItem()=="NOMBRE") {
                    dwhere=dwhere+" and Paquete.Nombre='"+JTXTNOM.getText()+"'";
                    DinamcReport.mostrarRep(NOM_SER.getText(), col, ser, from, dwhere);
                }                
                if (jcServicio.getSelectedItem()=="PRECIO TOTAL") {
                    dwhere=dwhere+" and Paquete.PrecioTotal>="+Integer.valueOf(JTXTPRECIOTO.getText());
                    DinamcReport.mostrarRep(NOM_SER.getText(), col, ser, from, dwhere);
                }
            } catch (JRException ex) {
                Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
            }
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione Datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_DDGENERARActionPerformed

